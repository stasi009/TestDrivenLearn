package demo

import java.util.Calendar

object DateTimeDemo extends App {

  def demoCalendar() = {
    val c = Calendar.getInstance()
    c.clear()
    
    println(c.get(Calendar.YEAR))
    println(c.get(Calendar.MONTH))
    println(c.get(Calendar.DAY_OF_MONTH))
    println(c.get(Calendar.HOUR_OF_DAY))
    println(c.get(Calendar.MINUTE))
    println(c.get(Calendar.SECOND))
    println(c.get(Calendar.MILLISECOND))
    println(c.getTimeInMillis)
  }

  override def main(args: Array[String]) = {
    demoCalendar()
  } //main

}